import type { Result } from "../types";
import type { Handler } from "../handler";

/**
 * Base class for defining an API service.
 */
export abstract class Service {
    constructor(private readonly handler: Handler) {}
    protected abstract readonly base: string;

    protected async request<T>(endpoint: string, data?: Record<string, any>): Promise<Result<T>> {
        return this.handler.request(`${this.base}/${endpoint}`, data);
    }
}
